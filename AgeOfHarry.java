// Create AgeOfHarry class to compute current age of harry
public class AgeOfHarry{
    public static void main(String[] args) {
        // Create a variable birthYear to indicate birth year of harry
        int birthYear = 2000;
        // Create a variable currentYear to indicate current year 
        int currentYear = 2024;
        // Create a variable currentAge to calculate current age  
        int currentAge = currentYear - birthYear;
        // Print the current age
        System.out.println("Harry's age in 2024 is "+currentAge);
    }
}